import sympy

# Определяем диапазон
start = 2**50
end = start + 10000

# Находим все простые числа в диапазоне
primes = list(sympy.primerange(start, end))

# Подсчитываем пары простых чисел, разделенных одним составным числом
count = 0

# Проверяем каждую возможную пару простых чисел
for i in range(len(primes) - 1):
    if primes[i + 1] - primes[i] == 2:
        count += 1

# Выводим количество пар
print("Количество пар простых чисел, разделенных одним составным числом:", count)
